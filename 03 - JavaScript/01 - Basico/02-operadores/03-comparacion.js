let a = 5;

//Relacionales
console.log(a >5 );
console.log(a >=5 );
console.log(a <5 );
console.log(a <=5 );

//Igualdad 
// == solo compara valores, no tipo de dato
console.log(a == 5)
console.log(a != 5)
console.log(a == "5")
console.log(a != "5")
// === tambien compara tipo de dato
console.log(a === "5")
console.log(a !== "5")
