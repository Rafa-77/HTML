// Declaraciones
let a = 5;
let b = 7;

// Expresion
console.log(a + b, "adicion");
console.log(a - b, "resta"); 
console.log(a * b, "multiplicacion"); 
console.log(a / b, "division"); 
console.log(a % b, "modulo"); 
console.log(a ** b, "potencia"); 

//Operador incrementar: 
console.log(a)
console.log(++a, "incrmenta, a"); //Incrementa a por 1 y muestra en la consola
console.log(a++, "a, incrementa"); //Muestra en la consola y luego incrementa el valor de a
console.log(a)

//Operador decrementar: 
console.log(b)
console.log(--b, "decrmenta, b"); //Decrementa b por 1 y muestra en la consola
console.log(b--, "b, decrementa"); //Muestra en la consola y luego incrementa el valor de b
console.log(b)